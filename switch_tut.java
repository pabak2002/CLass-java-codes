public class switch_tut {
    public static void main(String args[])
    {
        int a=2;
        switch(a)
        {
            case 1:
                System.out.println("This is one");
                break;
            case 2:
                System.out.println("This is two");
                break;
            case 3:
                System.out.println("This is three");
                break;
            default:
                System.out.println("This is none");
                break;
        }
    }
}
